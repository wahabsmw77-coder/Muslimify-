export function getExpandedAzkar(languageCode: string): AzkarItem[] {
  // Return the list with translation or fallback
  return STATIC_AZKAR.map(item => {
    // Copy item to avoid mutating static array
    const translatedText = item.translations[languageCode] || item.translations["en"] || "Translation coming soon";
    const translatedBenefit = item.benefits[languageCode] || item.benefits["en"] || "Virtue mentioned in authentic Sunnah records.";
    return {
      ...item,
      translations: { [languageCode]: translatedText },
      benefits: { [languageCode]: translatedBenefit }
    };
  });
}
